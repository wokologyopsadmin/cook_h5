// ── Core helpers ──────────────────────────────────────

function getLang() {
  return localStorage.getItem('chefhub_lang') || 'zh';
}

function t(key) {
  const lang = getLang();
  return (I18N[lang] && I18N[lang][key] !== undefined)
    ? I18N[lang][key]
    : (I18N.zh[key] !== undefined ? I18N.zh[key] : key);
}

function applyI18n() {
  const lang = getLang();
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang;

  // textContent replacements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = t(key);
    } else {
      el.textContent = t(key);
    }
  });

  // placeholder-only replacements
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-ph'));
  });

  // title attribute replacements
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.getAttribute('data-i18n-title'));
  });

  // Update language button active states
  document.querySelectorAll('[data-lang-btn]').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang-btn') === lang);
  });

  // Update document <title>
  const titleKey = document.body.getAttribute('data-page-title');
  if (titleKey) {
    document.title = 'Wokology — ' + t(titleKey);
  }
}

function setLanguage(lang) {
  localStorage.setItem('chefhub_lang', lang);
  applyI18n();
  // Re-render any dynamic content that uses t()
  if (typeof onLangChange === 'function') onLangChange(lang);
}

document.addEventListener('DOMContentLoaded', applyI18n);
